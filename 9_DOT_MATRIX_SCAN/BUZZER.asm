BUZZER EQU 0FFEFH

	ORG 8000H

INIT:
	MOV R0, #20

LOOP: 	MOV A, #10000000B
	CALL BUZ
	CALL DELAY

	MOV A, #00000000B
	CALL BUZ
	CALL DELAY
	CALL DELAY

	DJNZ R0, LOOP

	MOV R0, #20
	CALL longDelay
	CALL longDelay

	JMP LOOP

BUZ:	MOV DPTR, #BUZZER
	MOVX @DPTR, A
	RET

DELAY: 	MOV R7, #01FH
DELAY1: MOV R6, #0FFH
DELAY2: DJNZ R6, DELAY2
	DJNZ R7, DELAY1
	RET


longDelay:
	MOV R3, #0FFH
DELAY: 	MOV R4, #0FFH
DELAY1: MOV R5, #0FFH
DELAY2	MOV R6, #0FFH
DELAY3: MOV R7, #0FFH
DELAY4: DJNZ R7, DELAY4
	DJNZ R6, DELAY3
	DJNZ R5, DELAY2
	DJNZ R4, DELAY1
	DJZN R3, DELAY
	RET

END