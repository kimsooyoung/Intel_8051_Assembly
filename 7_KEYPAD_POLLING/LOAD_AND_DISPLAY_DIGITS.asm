	ORG 8000H
	DATAOUT EQU 0FFF0H
	DATAIN	EQU 0FFF1H
	
	DIGITMEM EQU 20H
	
	SEG1 EQU 0FFC3h
	SEG2 EQU 0FFC2h
	SEG3 EQU 0FFC1H

START:
	MOV R0, #DIGITMEM

	MOV @R0, #80H
	INC R0
	MOV @R0, #51H
	INC R0
	MOV @R0, #00H
	INC R0

mainLoop:
	CALL display
	JMP mainLoop

display:
	MOV A, #DIGITMEM
	MOV R0, A
	MOV A, @R0

	MOV DPTR, #SEG1
	MOVX @DPTR, A
	
	MOV A, R0
	INC A
	MOV R0, A
	MOV A, @R0
	
	MOV DPTR, #SEG2
	MOVX @DPTR, A

	MOV A, R0
	INC A
	MOV R0, A
	MOV A, @R0
	
	MOV DPTR, #SEG3
	MOVX @DPTR, A

	RET

END